#!/usr/bin/env node

import { brotliCompressSync } from 'node:zlib';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const publicDir = path.join(root, 'public');
const fontsDir = path.join(publicDir, 'fonts');
const allFontsPath = path.join(publicDir, 'sdkjs/common/AllFonts.js');
const manifestPath = path.join(publicDir, 'onlyoffice-browser-font-assets.json');
const selectionPath = path.join(publicDir, 'server/FileConverter/bin/font_selection.bin');
const imageDir = path.join(publicDir, 'sdkjs/common/Images');

const FONT_FILES = [
  'DejaVuSans.ttf',
  'DejaVuSans-Oblique.ttf',
  'DejaVuSans-Bold.ttf',
  'DejaVuSans-BoldOblique.ttf',
  'DejaVuSansMono.ttf',
  'DejaVuSansMono-Oblique.ttf',
  'DejaVuSansMono-Bold.ttf',
  'DejaVuSansMono-BoldOblique.ttf',
  'LiberationSans-Regular.ttf',
  'LiberationSans-Italic.ttf',
  'LiberationSans-Bold.ttf',
  'LiberationSans-BoldItalic.ttf',
  'ComicNeue-Regular.ttf',
  'ComicNeue-Italic.ttf',
  'ComicNeue-Bold.ttf',
  'ComicNeue-BoldItalic.ttf',
  'NotoSansSC-VF.ttf',
  'NotoSansTC-VF.ttf',
  'NotoSansJP-VF.ttf',
  'NotoSansKR-VF.ttf',
  'NotoSerifSC-VF.ttf',
  'NotoSerifTC-VF.ttf',
  'NotoSerifJP-VF.ttf',
  'NotoSerifKR-VF.ttf',
];

const fileIndex = new Map(FONT_FILES.map((name, index) => [name, index]));

function indexes(regular, italic = regular, bold = regular, boldItalic = bold) {
  return [regular, 0, italic, 0, bold, 0, boldItalic, 0];
}

function idx(name) {
  const value = fileIndex.get(name);
  if (value === undefined) {
    throw new Error(`Unknown font file in registry: ${name}`);
  }
  return value;
}

const SANS = indexes(
  idx('LiberationSans-Regular.ttf'),
  idx('LiberationSans-Italic.ttf'),
  idx('LiberationSans-Bold.ttf'),
  idx('LiberationSans-BoldItalic.ttf'),
);
const DEJAVU = indexes(
  idx('DejaVuSans.ttf'),
  idx('DejaVuSans-Oblique.ttf'),
  idx('DejaVuSans-Bold.ttf'),
  idx('DejaVuSans-BoldOblique.ttf'),
);
const MONO = indexes(
  idx('DejaVuSansMono.ttf'),
  idx('DejaVuSansMono-Oblique.ttf'),
  idx('DejaVuSansMono-Bold.ttf'),
  idx('DejaVuSansMono-BoldOblique.ttf'),
);
const COMIC = indexes(
  idx('ComicNeue-Regular.ttf'),
  idx('ComicNeue-Italic.ttf'),
  idx('ComicNeue-Bold.ttf'),
  idx('ComicNeue-BoldItalic.ttf'),
);
const CJK_SC = indexes(idx('NotoSansSC-VF.ttf'));
const CJK_TC = indexes(idx('NotoSansTC-VF.ttf'));
const CJK_JP = indexes(idx('NotoSansJP-VF.ttf'));
const CJK_KR = indexes(idx('NotoSansKR-VF.ttf'));
const SERIF_SC = indexes(idx('NotoSerifSC-VF.ttf'));
const SERIF_TC = indexes(idx('NotoSerifTC-VF.ttf'));
const SERIF_JP = indexes(idx('NotoSerifJP-VF.ttf'));
const SERIF_KR = indexes(idx('NotoSerifKR-VF.ttf'));

const FONT_INFOS = [
  ['Arial', ...SANS],
  ['Aptos', ...SANS],
  ['Calibri', ...SANS],
  ['Cambria', ...SERIF_SC],
  ['Cambria Math', ...DEJAVU],
  ['Comic Neue', ...COMIC],
  ['Comic Sans MS', ...COMIC],
  ['Consolas', ...MONO],
  ['Courier New', ...MONO],
  ['DejaVu Sans', ...DEJAVU],
  ['DejaVu Sans Mono', ...MONO],
  ['DengXian', ...CJK_SC],
  ['FangSong', ...SERIF_SC],
  ['KaiTi', ...SERIF_SC],
  ['Liberation Sans', ...SANS],
  ['Microsoft YaHei', ...CJK_SC],
  ['Microsoft YaHei UI', ...CJK_SC],
  ['NSimSun', ...SERIF_SC],
  ['Noto Sans JP', ...CJK_JP],
  ['Noto Sans KR', ...CJK_KR],
  ['Noto Sans SC', ...CJK_SC],
  ['Noto Sans TC', ...CJK_TC],
  ['Noto Serif JP', ...SERIF_JP],
  ['Noto Serif KR', ...SERIF_KR],
  ['Noto Serif SC', ...SERIF_SC],
  ['Noto Serif TC', ...SERIF_TC],
  ['Segoe UI', ...SANS],
  ['SimHei', ...CJK_SC],
  ['SimSun', ...SERIF_SC],
  ['SimSun-ExtB', ...SERIF_SC],
  ['Symbol', ...DEJAVU],
  ['Tahoma', ...SANS],
  ['Times New Roman', ...SERIF_SC],
  ['Wingdings', ...DEJAVU],
];

const visibleNames = [
  'Aptos',
  'Arial',
  'Calibri',
  'Cambria',
  'Comic Neue',
  'Consolas',
  'Courier New',
  'Microsoft YaHei',
  'Noto Sans SC',
  'Noto Serif SC',
  'SimSun',
  'Times New Roman',
];

const fallbackRangeIndex = FONT_INFOS.findIndex(([name]) => name === 'Noto Sans SC');
if (fallbackRangeIndex < 0) {
  throw new Error('Noto Sans SC fallback range is missing');
}

const allFontsSource = `/*
 * Generated by scripts/generate-local-onlyoffice-font-assets.mjs.
 * Uses the open-source fonts committed under public/fonts and maps common
 * Office font names to those deployed files for the browser runtime.
 */
window.__all_fonts_js_version__ = 2;
window.__fonts_files = ${JSON.stringify(FONT_FILES, null, 2)};
window.__fonts_infos = ${JSON.stringify(FONT_INFOS, null, 2)};
window.__fonts_visible_names = ${JSON.stringify(visibleNames, null, 2)};
window.__fonts_ranges = ${JSON.stringify([0, 0x10ffff, fallbackRangeIndex])};
window.g_fonts_selection_bin = '';
`;

const transparentPng = Buffer.from(
  'iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAYAAAAfFcSJAAAADUlEQVR42mP8z8BQDwAFgwJ/lqT3rQAAAABJRU5ErkJggg==',
  'base64',
);
const thumbnailPaths = [
  'sdkjs/common/Images/fonts_thumbnail.png',
  'sdkjs/common/Images/fonts_thumbnail@1.25x.png',
  'sdkjs/common/Images/fonts_thumbnail@1.5x.png',
  'sdkjs/common/Images/fonts_thumbnail@1.75x.png',
  'sdkjs/common/Images/fonts_thumbnail@2x.png',
  'sdkjs/common/Images/fonts_thumbnail_ea.png',
  'sdkjs/common/Images/fonts_thumbnail_ea@1.25x.png',
  'sdkjs/common/Images/fonts_thumbnail_ea@1.5x.png',
  'sdkjs/common/Images/fonts_thumbnail_ea@1.75x.png',
  'sdkjs/common/Images/fonts_thumbnail_ea@2x.png',
];

for (const fontFile of FONT_FILES) {
  const fontPath = path.join(fontsDir, fontFile);
  if (!fs.existsSync(fontPath)) {
    throw new Error(`Missing font file: ${fontPath}`);
  }
}

fs.mkdirSync(path.dirname(allFontsPath), { recursive: true });
fs.writeFileSync(allFontsPath, allFontsSource);
fs.writeFileSync(`${allFontsPath}.br`, brotliCompressSync(Buffer.from(allFontsSource)));

fs.mkdirSync(imageDir, { recursive: true });
for (const relativePath of thumbnailPaths) {
  fs.writeFileSync(path.join(publicDir, relativePath), transparentPng);
}

fs.mkdirSync(path.dirname(selectionPath), { recursive: true });
if (!fs.existsSync(selectionPath) || fs.statSync(selectionPath).size === 0) {
  fs.writeFileSync(selectionPath, Buffer.alloc(4));
}

const manifest = {
  version: 1,
  generator: 'local-open-font-registry',
  fontSet: 'office-agent-open-source',
  generatedAt: new Date().toISOString(),
  allFonts: 'sdkjs/common/AllFonts.js',
  fontSelection: 'server/FileConverter/bin/font_selection.bin',
  fontThumbnails: thumbnailPaths,
  fonts: FONT_FILES.map((fontFile) => `fonts/${fontFile}`),
};

fs.writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);
console.log(`Generated ${manifest.fonts.length} font files in ${manifestPath}`);
